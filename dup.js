// Toplama fonksiyonları
function add(a, b) { return a + b; }
function sum(x, y) { return x + y; }
function total(n1, n2) { return n1 + n2; }
function calculateSum(a, b) { return a + b; }

// Çıkarma fonksiyonları
function subtract(a, b) { return a - b; }
function difference(x, y) { return x - y; }
function diff(n1, n2) { return n1 - n2; }
function calculateDiff(a, b) { return a - b; }

// Çarpma fonksiyonları
function multiply(a, b) { return a * b; }
function product(x, y) { return x * y; }
function times(n1, n2) { return n1 * n2; }
function calculateProduct(a, b) { return a * b; }

// Bölme fonksiyonları
function divide(a, b) { return a / b; }
function quotient(x, y) { return x / y; }
function split(n1, n2) { return n1 / n2; }
function calculateQuotient(a, b) { return a / b; }

